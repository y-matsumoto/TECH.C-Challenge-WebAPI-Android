package entity;

public class ZipAddressEntity extends BaseEntity {
	private String stateKana;
	private String cityKana;
	private String addressKana;
	private String companyKana;
	private String state;
	private String city;
	private String address;
	private String company;
	
	/**
	 * stateKanaを取得します。
	 * @return stateKana
	 */
	public String getStateKana() {
	    return stateKana;
	}
	/**
	 * stateKanaを設定します。
	 * @param stateKana stateKana
	 */
	public void setStateKana(String stateKana) {
	    this.stateKana = stateKana;
	}
	/**
	 * cityKanaを取得します。
	 * @return cityKana
	 */
	public String getCityKana() {
	    return cityKana;
	}
	/**
	 * cityKanaを設定します。
	 * @param cityKana cityKana
	 */
	public void setCityKana(String cityKana) {
	    this.cityKana = cityKana;
	}
	/**
	 * addressKanaを取得します。
	 * @return addressKana
	 */
	public String getAddressKana() {
	    return addressKana;
	}
	/**
	 * addressKanaを設定します。
	 * @param addressKana addressKana
	 */
	public void setAddressKana(String addressKana) {
	    this.addressKana = addressKana;
	}
	/**
	 * companyKanaを取得します。
	 * @return companyKana
	 */
	public String getCompanyKana() {
	    return companyKana;
	}
	/**
	 * companyKanaを設定します。
	 * @param companyKana companyKana
	 */
	public void setCompanyKana(String companyKana) {
	    this.companyKana = companyKana;
	}
	/**
	 * stateを取得します。
	 * @return state
	 */
	public String getState() {
	    return state;
	}
	/**
	 * stateを設定します。
	 * @param state state
	 */
	public void setState(String state) {
	    this.state = state;
	}
	/**
	 * cityを取得します。
	 * @return city
	 */
	public String getCity() {
	    return city;
	}
	/**
	 * cityを設定します。
	 * @param city city
	 */
	public void setCity(String city) {
	    this.city = city;
	}
	/**
	 * addressを取得します。
	 * @return address
	 */
	public String getAddress() {
	    return address;
	}
	/**
	 * addressを設定します。
	 * @param address address
	 */
	public void setAddress(String address) {
	    this.address = address;
	}
	/**
	 * companyを取得します。
	 * @return company
	 */
	public String getCompany() {
	    return company;
	}
	/**
	 * companyを設定します。
	 * @param company company
	 */
	public void setCompany(String company) {
	    this.company = company;
	}
	
}
